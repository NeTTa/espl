#include <stdio.h>
int main (int argc, char* argv[]){
  
  int hsvmhr (char* const, char* const);
  
  if ( hsvmhr(argv[1], argv[2]) > 0 )
    printf("%s is BIGGER then %s\n", argv[1], argv[2]);
  else if ( hsvmhr(argv[1], argv[2]) < 0 )
    printf("%s is SMALLER then %s\n", argv[1], argv[2]);
  else printf("HOORAY!\t the two strings are identical\n");
  
  return 0;
}